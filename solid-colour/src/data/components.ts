/* ===========================================================
   Atlas — featured + community components.

   Each entry is a real OSS UI project on GitHub. The trending
   grid pulls the top N by `stars`; the detail / Visit link uses
   `url`; preview images are resolved by the PreviewImage component
   from `github` (owner/repo) via opengraph.githubassets.com with
   a microlink + hashed-gradient fallback chain.

   Stats are point-in-time snapshots curated by hand (see
   scripts/scrape-awesome.ts for the future automated path).
   Anything below ~500 stars or stale > 18 months is excluded.
   =========================================================== */

export type ComponentCategory =
  | 'Hero'
  | 'Pricing'
  | 'Auth'
  | 'Dashboard'
  | 'Navigation'
  | 'Forms'
  | 'AI Chat'
  | 'Buttons'
  | 'Modals'
  | 'Cards'
  | 'Animation';

export interface ComponentEntry {
  id: string;
  name: string;
  category: ComponentCategory;
  /** GitHub owner — also used as the displayed author. */
  author: string;
  authorInitials: string;
  authorColor: string;
  framework: 'React' | 'Vue' | 'Svelte' | 'Solid';
  styling: 'Tailwind' | 'CSS-in-JS' | 'CSS';
  /** GitHub stars (point-in-time, hand-curated). */
  stars: number;
  /** Last commit ISO date (point-in-time). */
  pushedAt: string;
  /** SPDX license id, when known. */
  spdxLicense?: string;
  /** Short one-liner shown under the name on cards. */
  tagline: string;
  /** Canonical product URL, falls back to GitHub repo. */
  url: string;
  /** GitHub `owner/repo` — drives preview image + Visit link fallback. */
  github: string;
  /** Optional fallback gradient used while/if the preview image fails. */
  thumb?: string;
  featured?: boolean;
  community?: boolean;
}

